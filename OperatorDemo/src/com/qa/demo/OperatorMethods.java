package com.qa.demo;

public class OperatorMethods {
	
	public static float addNumbers (float num1, float num2) {
		return num1 + num2;
	}
	public static float subNumbers(float num1, float num2) {
		return num1 - num2;
	}
	public static float timesByNum(float num1, float num2) {
		return num1 * num2;
	}
	public static float divideNumbers(float num1, float num2) {
		return num1 / num2;
	}
	
	public static float modNumbers(float num1, float num2) {
		return num1 % num2;
	}
	public static float exponetial(float num1, float num2) {
		return (float) Math.pow(num1, num2);
	}

}
